public abstract class Personaggio {
    protected int vita;
    protected int energia;
    protected int ad;
    protected int ap;
    protected Boolean dead;
    public Personaggio(int vita,int energia, int ad, int ap,Boolean dead){

        this.vita=vita;
        this.energia=energia;
        this.ad=ad;
        this.ap=ap;
        this.dead=dead;
    }




    }


